          <h2 style="margin-top: 60px;">The Optical Brighteners and their increasing demand</h2>
          <br> <br>
          <div class="col-div col-xs-12 col-lg-12 content-area-layout">
              <p style="margin-top: 20px;">What we see as color of an object is not something inherent in it but reflection of light from its surface. Newton observed that surface of an object reflects some light rays and absorbs the rest, and the reflected rays are then interpreted by our brain as the color of that object. 
So, color is actually our perception. The red color of an apple is the reflection of light rays with certain frequency and absorption of others. An object appears white when it reflects all rays and black when it absorbs all.
Optical Brightening Agents or OBA works in this principle. Also known as the fluorescent agent this whitening medium has become a big industry itself as it is being used extensively in many industries including fabrics, papers, detergents, plastics, paints, etc.
 </p>
              
            </div>
<ul style="list-style-type:circle;">
<li><strong>Science of OBAs : <br></strong><a href="http://www.valblanc.com/optical-brighteners/" style="color: #2a2c74; font-weight: 600;">Optical Brightening Agent</a> or Fluorescent Brightening Agent is a chemical compound that whiten and brighten the object on which it is applied by creating a luminescence effect.
<br>Light rays when incident on objects comprise different wavelengths and frequencies, are divided into visible and invisible bands. Our known colors are the reflected rays from the visible region. Whites in general are not white but slightly off white or with yellowish hue even after washing and bleaching, because the substrate levels are not able to reflect maximum rays.<br>
OBAs absorb some of the invisible rays from the UV band from the incident light and convert them into visible blue light. As the blue light reflection increases it creates an effect called fluorescence. Therefore white becomes glowing white and colored fabrics becomes brighter.
 </li>

<li><strong>Why bleaching or bluing is not good enough? <br></strong> The commercial value of fabrics depends upon the degree of whiteness or brightness. Coloring of textiles results in some impurities which reduce the density of color. What bleaching does is that it removes these impurities or converts them into non-color substances. This way only the anti effects of impurities get reduced by making them invisible to human eyes but  
In case of blue tinting the yellow cast of substrates is neutralised by using blue or blue violet dyes. Optically speaking the reflection of light rays from the blue region will be increased to supersede the the reflection of yellow rays. As a result the fabric will appear as somewhat  greyish-white to our eyes.
</li>
<li><strong>The ever increasing market of OBA : <br></strong>  In textile industry : Brightening agents are used extensively at various stages in the textile industry. It helps clothings to appear new and brighter for a long time and therefore play a key role in pricing of the clothings. Optical brighteners have vast uses for whitening and brightening of wool, linen, silk and cotton and polyester.<br>
In laundry detergent: As the brightening agent used in textiles subside through washing, <a href="http://www.valblanc.com/" style="color: #2a2c74; font-weight: 600;">OBAs</a> present in laundry detergent help maintain shine.<br>
In paper industry: Brighteners are used heavily in the paper industry to achieve high level of brightness.<br>
In cosmetic industry: Brighteners used in several makeup products used for face and eye. Also used in shampoos, conditioners and hair dyes.
</li>
