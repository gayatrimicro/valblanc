            <h2 style="margin-top: 60px;">What environmental effects do optical brighteners have?</h2>
          <br> <br>
          <div class="col-div col-xs-12 col-lg-12 content-area-layout">
              <p style="margin-top: 20px;">A research study done by the Environmental Protection Agency (EPA) in 1975 raised doubts about the environmental and health effects of optical brighteners. They recorded that over 29,000,000 lbs. of U.S. produced optical brighteners went into laundry detergents in 1969, probably much more is getting dumped into laundry detergents today and ending up in our water.</p>
              <p>Today the Environmental Protection Agency (EPAâ€™s) DFE program invites laundry developers to partner with them to act as environmental representatives by making products that are more eco-friendly. For optical brighteners, the program motivates manufacturers to use those that pose "low toxicity to humans and the environment both as parent chemicals and degradation by-products."</p>
              <p>Optical brighteners are not readily biodegradable and can bio-accumulate, so they pose a potential hazard to aquatic life. Also, according to a 2011 report by the European Ecolabel Commission on criteria for laundry detergents, as optical brighteners undergo photo-degradation, numerous metabolites can be produced that are not yet identified, which means we may not know the true potential impacts upon the environment.</p>
              <p>Given these questions regarding environmental effects and the huge amounts of these chemicals are being used today, it seems wise to cut back on our usage of them when we can.</p>             
            </div>

            <h2 style="margin-top: 60px;">What are the common uses of optical brighteners?</h2>
          <br> <br>
          <div class="col-div col-xs-12 col-lg-12 content-area-layout">
              <p style="margin-top: 20px;">Optical Brighteners are commonly added to laundry detergents to make the clothes look cleaner. Usually cleaned laundry looks yellowish, which consumers do not like. Optical brighteners have replaced bluing which was previously used to produce the same effect.</p>
              <p>Brighteners are used in many papers, especially high brightness papers, resulting in their strongly fluorescent appearance under UltraViolet illumination. Paper brightness is generally measured at 457 nm, well within the fluorescent activity range of brighteners. Paper used for banknotes do not contain optical brighteners, so a common method for detecting counterfeit notes is to check for fluorescence.</p>
              <p>Optical brighteners have also found use in cosmetics. One implementation is to formulas for washing and conditioning grey or blonde hair, where the brightener can not only increase the luminance and sparkle of the hair, but can also perfect dull, yellowish discoloration without darkening the hair. Some of the advanced face and eye powders contain optical brightener microspheres that brighten shadowed or dark areas of the skin, such as "tired eyes".</p>

              <img src="../../assets/images/blog/opt5.jpg"  style="max-width: 100%;">           
