          <h2 style="margin-top: 60px;">Brief About Optical Brightening Agents &amp; Their Uses</h2>
          <br> <br>
          <div class="col-div col-xs-12 col-lg-12 content-area-layout">
              <p style="margin-top: 20px;">OBAs are chemical compounds that play with colors. As the name suggests it is self-explanatory that Optical Brighteners are those brighteners operate the visible part of the electromagnetic spectrum, especially in relation to the action of light and colors.</p>
              <p>Optical Brighteners are those chemical compounds which when applied on any substance absorbs the high frequency lights (colors) such as Brown, Yellow and reflects the low frequency lights (colors) such as violet, indigo and Blue. OBAs use the invisible ultraviolet rays to reflect the Bluish Color
</p>
              
            </div>
<ul style="list-style-type:circle;">
<li><strong> Optical Brightener in Textile Industries : </strong>
Naturally, no cloth is in pure white color. The raw material in the textile industries are in grayish color which is known as greige. They consist all the impurities. These impurities include the residue of pesticides and fertilizers. Whereas, in case of material procured from animal consists of natural oil, natural secretions and dusts. These impurities make it difficult to dye the fabric. Therefore, every cloth undergoes bleaching treatment. This treatment is bleaching treatment. There are two type of bleaching i.e. oxidative bleaching and reductive bleaching.<br><br>
The other way to brighten and enhance the whiteness of your cloth can be done with the help of optical brightening agent. Optical brighteners are chemical agents that are used to make the white cloth as brightest white in <a href="http://www.valblanc.com/blog/benefits-of-optical-brighteners-for-textile-industries/" style="color: #2a2c74; font-weight: 600;">textile industries</a>.</li>

<li><strong>Optical Brightener in Laundry Detergent : </strong> 
It is natural that after sometimes the whiteness of the cloth becomes dull. However, Bleaching time and again can turn your white cloth into yellow. And excess scrubbing can damage the cloth, leading to creation of excess lint. In order to avoid any further spoilage of it is suggested to utilize optical brightener ob.<br><br>
Optical brighteners which are normally used in laundry detergent can help in converting the low density colors of the cloth such as yellow and brown color into high density color i.e blue and violet. Optical Brighteners for detergent can help in making your clothes bright white.
Valblanc is one of the <a href="http://www.valblanc.com" style="color: #2a2c74; font-weight: 600;">Optical Brightener Manufacturers</a> in India and has received lots of awards. Valblanc also provide quality assurance of their products.

</li>
<li><strong>Optical Brighteners in Paper : </strong> 
Paper is produced from fermented bamboo sticks. Bamboo sticks and woods are not in white color as too consist impurities. However, the outcome of the process ends into pure white. This is due to the chemical reaction of Brightening Agent. In-order to make the papers look bright and neat it is common practice of the manufacturers to use Optical Brighteners. OBAs are being used since ages in Paper Factories and Textile Industries.<br><br>
Similarly recycled papers also require good amount of OBA to improve its color and look clean. It must be noted that the recycled paper should not comprise of excess of optical brightener, or else it may affect its appearance by making it look spotty and vary in brightness. A proper check with UV source light can help avoiding such situation.

</li>
  <li><strong>Optical Brighteners in Plastics :  </strong> 
Plastics are basically polymer compounds. In the fast moving world plastics are dominating all the metals. World has become plastic as everyone opt for because it is pocket friendly and so plastics are in trend. Be it some decorative, toys, accessories or any essential commodity. Plastic are in rule. In original form plastics are made of polyurethane compound and are totally discoloured. Hence, to make it more attractive and eye-catching manufacturers have starting implementing OBA in plastics as well.<br><br>
Also Now-a-days people are crazy in utilizing Fluorescent Whitened plastics such as water bottles, toys or any plastic hardware that goes eye-catching and attracts people. </li> 

    <li><strong> Optical Brighteners in Paints :</strong> 
In olden days lime surface was the paint for the walls of the houses and buildings Due to technological changes, it later changed to paints which were expensive and came the oil paints, then came paints that we washable. And now we have texture paints and plastic emulsion paints which has a coat of plastic<br><br>
Plastic paints can be used in the interior as well as exterior of the house. It give a matte, glossy or semi glossy finishing. Apart from the texture of the paint, to enhance the color Optical brighteners are a must. <a href="http://www.valblanc.com/optical-brighteners/" style="color: #2a2c74; font-weight: 600;">Optical Brighteners</a> are used in every form of paints to enhance the appearance of the freshly painted walls by reflecting bright color. Optical brightener helps in producing brighter color than the paint’s original colour
<li><strong>Cosmetics :</strong>

Not widely known, even cosmetics have optical brightener in them. This includes shampoos and conditioner for blonde and grey hairs, to increase the hairs luminosity. Optical brighteners are also found in concealers and skin fairing agents. That covers the dark patches and reflects bright light colors.</li>
