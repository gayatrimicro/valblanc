            <h2 style="margin-top: 60px;">What are the end uses of optical brighteners?</h2>
          <br> <br>
          <div class="col-div col-xs-12 col-lg-12 content-area-layout">
              <ul>
                <li>Detergent whitener (as a substitute of bluing agents)</li>
                <li>Paper brightening (inside or in a coating)</li>
                <li>Fiber whitening (inside, added to polymer melts)</li>
                <li>Textile whitening (outside, added to fabric finishes)</li>
                <li>Color-correcting or brightening additive in advanced cosmetic formulas (shampoos, conditioners, eye makeup)</li>
              </ul>              
